        <h3 class="text-center">About IQAC</h3><hr>

      <p class="text-justify"> 

          Internal Quality Assurance Cell, or IQAC, helps higher education institutions streamline their operations and meet their objectives for learning outcomes.

          </p><br><p class="text-justify"> Every higher education institution should set up an Internal Quality Assurance Cell (IQAC) in accordance with the requirements of the National Assessment and Accreditation Council (NAAC) as a method of accreditation quality maintenance. Because quality improvement is a continual process, the IQAC aims to achieve the goals of quality improvement and maintenance by integrating itself into the institution's structure.

          </p><br><p class="text-justify"> The main responsibility of the IQAC is to develop a system for consciously, consistently, and catalytically improving institutions' overall performance. By internalizing a quality culture and institutionalizing best practices, IQAC seeks to promote behaviors that will help institutions function more effectively.

          </p>

          </p>

          <h3><strong>Objectives</strong></h3>

          <p class="text-justify">IQAC was founded to accomplish the following goals:</p>

          <p><ul>

            <li>To create a solid structure that improves the institution's academic and operational performance.</li>

            <li>Encouraging and promoting the use of best practices and human resources, and working to improve quality in all areas.</li>

            <li>To ensure that every aspect of the Institute is run with precision, openness, and a single-minded commitment.</li>

          </ul></p>

          <h3><strong>Strategies</strong></h3>

          <p class="text-justify">IQAC will play a key role in developing techniques, guidelines, and protocols for:</p>

          <p><ul>

            <li>Ascertain that all academic and administrative duties are carried out efficiently, according to schedule, and at the proper times.</li>

            <li>Ensuring that academic programs are of a high caliber and have application in the workplace.</li>

            <li>Use of innovative teaching and learning methods.</li>

            <li>Ensuring the services and systems of support that are required are available.</li>

            <li>Applying effective evaluation criteria.</li>

          </ul></p>

